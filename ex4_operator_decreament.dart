//operator decreament (pengurangan nilai dengan 1)
void main(List<String> args) {
int a = 9, b = a;
print('Pre-decreament');
print('Nilai a awal: $a');
print('--a: ${--a}');
print('Nilai a akhir: $a');
print('\nPost-decreament');
print('Nilai b awal: $b');
print('b--: ${b--}');
print('Nilai b akhir: $b');
}